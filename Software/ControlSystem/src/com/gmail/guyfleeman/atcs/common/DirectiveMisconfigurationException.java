public class DirectiveMisconfigurationException extends Exception
{
	public DirectiveMisconfigurationException()
	{
		super();
	}

	public DirectiveMisconfigurationException(String message)
	{
		super(message);
	}

	public DirectiveMisconfigurationException(Throwable cause)
	{
		super(cause);
	}

	public DirectiveMisconfigurationException(String message, Throwable cause)
	{
		super(message, cause);
	}
}
